    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            Ini ini = new Ini(new File("dwconfig.ini"));
            ini.put("Connection", "HOST", txtHost.getText());
            ini.put("Connection", "USER", txtUser.getText());
            ini.put("Connection", "PASS", txtPass.getText());
            ini.put("Connection", "DB", txtDb.getText());

            ini.store();
        } catch (IOException ex) {
            ex.printStackTrace();
        }

        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        HOST = txtHost.getText().toString();
        DB = txtDb.getText().toString();
        USER = txtPass.getText().toString();
        PASS = txtPass.getText().toString();
        if (HOST.equals("") || DB.equals("")) {
            return;
        }
        SimpleMySQL mysql = new SimpleMySQL();
        if (mysql.connect(HOST, USER, PASS, DB, "tis-620")) {
            lbSts.setForeground(Color.BLUE);
            lbSts.setText("Connected.");
        } else {
            lbSts.setForeground(Color.RED);
            lbSts.setText("Not Connect.");
        }
        mysql.close();



    }//GEN-LAST:event_jButton3ActionPerformed

